package entity;

public class Reader {
	private String password;
	private int readerPhone;
	private int idNumber;
	private int allowBookNum;
	private String readerID;
	private String readerType;
	private int currentnumber;
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public int getReaderPhone() {
		return readerPhone;
	}
	public void setReaderPhone(int readerPhone) {
		this.readerPhone = readerPhone;
	}
	public int getIdNumber() {
		return idNumber;
	}
	public void setIdNumber(int idNumber) {
		this.idNumber = idNumber;
	}
	public int getAllowBookNum() {
		return allowBookNum;
	}
	public void setAllowBookNum(int allowBookNum) {
		this.allowBookNum = allowBookNum;
	}
	public String getReaderID() {
		return readerID;
	}
	public void setReaderID(String readerID) {
		this.readerID = readerID;
	}
	public String getReaderType() {
		return readerType;
	}
	public void setReaderType(String readerType) {
		this.readerType = readerType;
	}
	public int getCurrentnumber() {
		return currentnumber;
	}
	public void setCurrentnumber(int currentnumber) {
		this.currentnumber = currentnumber;
	}
	
	

}
